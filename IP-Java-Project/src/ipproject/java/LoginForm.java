    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        String username = usernameInput.getText();
        String password = new String(passwordInput.getPassword());
        if (username.equals("") || password.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Please enter proper details");
            return ;
        }
        if(username.length()>20 || password.length()>20)
        {
            JOptionPane.showMessageDialog(null,"Credentials cannot be longer than 20 characters");
            return ;
        }
        try
        {
            Class.forName("java.sql.Driver");
            Connection c = DriverManager.getConnection(Utils.URL,Utils.USER,Utils.PASSWORD);
            Statement s = c.createStatement();
            ResultSet rs = s.executeQuery("select * from users");
            boolean doesUserExist = false;
            while (rs.next())
            {
                String u = rs.getString("username");
                if(u.equalsIgnoreCase("username"))
                {
                    doesUserExist = true;
                    break;
                }
            }
            if (!doesUserExist)
            {
                JOptionPane.showMessageDialog(null,"Please enter a registered username");
            }
            else if(password.equals(rs.getString("password")))
            {
                new LoggedInForm().setVisible(true);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Invalid password for user "+username);
            }
            rs.close();
            s.close();
            c.close();
        }
        catch (ClassNotFoundException | SQLException e)
        {
            if (DEBUG)
                e.printStackTrace();
            else
                JOptionPane.showMessageDialog(null,"Error occurred: "+e.getMessage());
            return ;
        }
        
    }//GEN-LAST:event_LoginButtonActionPerformed

